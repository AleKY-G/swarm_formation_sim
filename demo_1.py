# This simulation combines the consensus decision making, role assignment and loop formation
# into one integrated demonstration. Dispersed in random positions, the swarm aggregates
# together to make decision of which loop to form, then assign the target positions to the
# robots while they are still in an aggregated swarm. After the two consensus processes are
# done, the swarm disperses and aggregates again, this time aims to form a loop with each
# robot at its designated position. The climbing method is used to achieve this, the robots
# also dynamically adjust their local shapes when forming the loop. The above steps will be
# repeated infinitely.






